                String role = (String) jrole.getSelectedItem();


                if (lastName.isEmpty() || firstName.isEmpty() || username.isEmpty() || password.isEmpty() || role.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "All fields must be filled out", "Warning", JOptionPane.WARNING_MESSAGE);
                    return; 
                }

                if (registerUser(lastName, firstName, username, password, role)) {
                    JOptionPane.showMessageDialog(null, "Registration successful");
                   
                    new Login().setVisible(true);
                    dispose(); 
                } else {
                    JOptionPane.showMessageDialog(null, "Registration failed");
                }
