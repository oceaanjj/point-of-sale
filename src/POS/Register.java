    private boolean registerUser(String lastName, String firstName, String username, String password, String role) {
        String url = "jdbc:mysql://localhost:3306/POS"; 
        String dbUser = "root"; 
        String dbPassword = "@dmin001"; 

        try (Connection connection = DriverManager.getConnection(url, dbUser, dbPassword)) {
            String query = "INSERT INTO User (lastname, firstname, username, password, role) VALUES (?, ?, ?, ?, ?)";
            try (PreparedStatement pst = connection.prepareStatement(query)) {
                pst.setString(1, lastName);
                pst.setString(2, firstName);
                pst.setString(3, username);
                pst.setString(4, password);
                pst.setString(5, role);
                pst.executeUpdate();
                return true;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database connection error: " + e.getMessage());
            return false;
        }
    }
    
